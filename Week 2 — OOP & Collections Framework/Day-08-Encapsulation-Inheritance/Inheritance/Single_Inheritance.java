public class Single_Inheritance {
    // In single inheritance, a class inherits from only one parent class.
    // This is the most basic form of inheritance.
    // Example:

    class A {
        void methodA() {
            System.out.println("Method A");
        }
    }

    class B extends A {
        void methodB() {
            System.out.println("Method B");
        }
    }
}
