public class No_Argument_Constructor {
    // A no-argument constructor is a constructor that does not take any parameters. It is used to create an object without providing any initial values.
    // If you do not define any constructor in your class, the Java compiler automatically provides a default no-argument constructor.
    // However, if you define any constructor (like a parameterized constructor), the default no-argument constructor will not be provided automatically, and you will need to define it explicitly if you want to use it.

    // Example of a no-argument constructor
    public No_Argument_Constructor() {
        System.out.println("This is a no-argument constructor.");
    }
    
}
