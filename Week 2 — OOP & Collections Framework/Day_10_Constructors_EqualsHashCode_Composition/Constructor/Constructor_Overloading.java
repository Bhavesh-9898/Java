public class Constructor_Overloading {

    // No-argument constructor
    // This constructor is called when no arguments are passed during object creation.
    // It simply prints a message indicating that the no-argument constructor has been called.
    // Constructor overloading allows us to have multiple constructors in a class with different parameter lists.
    // In this example, we have a no-argument constructor and a parameterized constructor that takes an integer argument.

    public Constructor_Overloading() {
        System.out.println("No-argument constructor called.");
    }
    
}
