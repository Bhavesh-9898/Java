public class Default_Access_Modifier {
    // Default access modifier is also known as package-private access modifier. 
    // It is the default access level for classes, methods, and variables that do not have an explicit access modifier. 
    // It allows access to the members of the class within the same package but not from outside the package.
    // It is denoted by the absence of any access modifier keyword (public, private, protected).


    Default_Access_Modifier() {
        System.out.println("This is a default access modifier constructor");
    }
    
}
