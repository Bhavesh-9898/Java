public class Super {
    // The super keyword in Java is used to refer to the immediate parent class of a subclass. 
    // It can be used to access the parent class's members (variables and methods) and constructors. 
    // The super keyword is commonly used in the following scenarios:
    // 1. To call the parent class's constructor from the subclass's constructor.
    // 2. To access the parent class's instance variables when they are hidden by the subclass's variables

    Super() {
        System.out.println("This is the parent class constructor");
    }
}
