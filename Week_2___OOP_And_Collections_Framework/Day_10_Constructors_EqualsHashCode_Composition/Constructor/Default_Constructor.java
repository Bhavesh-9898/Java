public class Default_Constructor {
    // A default constructor is a constructor that takes no arguments and is automatically provided by the Java compiler if no other constructors are defined in the class.
    // It initializes the object with default values (e.g., null for objects, 0 for numeric types, false for boolean).
    // If you define any constructor (like a parameterized constructor), the default constructor will not be provided automatically, and you will need to define it explicitly if you want to use it.

    // Example of a default constructor
    public Default_Constructor() {
        System.out.println("This is the default constructor.");
    }
    
}
