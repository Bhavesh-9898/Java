public class Logical_Operator {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        // Logical AND
        boolean andResult = a && b; // Result: false
        System.out.println("Logical AND: " + andResult);

        // Logical OR
        boolean orResult = a || b; // Result: true
        System.out.println("Logical OR: " + orResult);

        // Logical NOT
        boolean notResult = !a; // Result: false
        System.out.println("Logical NOT of a: " + notResult);
    }

    
}
