public class Enhanced_for_Loop_for_each {
    public static void main(String[] args) {
        // Enhanced for loop, also known as for-each loop, is a simplified version of the for loop that is used to iterate over arrays and collections. 
        // It eliminates the need for an index variable and provides a more readable syntax. 
        // The syntax of an enhanced for loop is:

        // Using an enhanced for loop to iterate over an array of integers
        int[] numbers = {1, 2, 3, 4, 5};
        for (int number : numbers) {
            System.out.println(number);
        }
    }
    
}
